# list and tuples
# lists are the container to store a set of values of any datatypes
friends = ["Apple", "orange", 5, 3.908, False, "Akash"]
print(friends[0])
# we can do this thing with the help of string but if we want to change the value of the string we cannot do that ; that's why strings are immutable but with the help of list we can do that
friends[0] = "komal"
print(friends[0])
